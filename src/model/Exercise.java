package model;


public class Exercise {

    public String name;
    public String bodyPart;
    public String level;
    public double caloriesPerMin;
    public String tips;

    public Exercise(String name,String bodyPart,String level,double caloriesPerMin){
        this.name = name;
        this.bodyPart = bodyPart;
        this.level = level;
        this.caloriesPerMin = caloriesPerMin;
        this.tips = tips;

    }
}
